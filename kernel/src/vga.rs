fn vga_vmemwr(dst_off: u64, src: u128, base_addr: u64) {
    let dst = (base_addr + dst_off) as *mut u128;

    unsafe {
        core::ptr::write_volatile(dst, src);
    }
}

fn vga_set_plane(p: u8) {
    let pp = p & 3;
    let pmask = 1 << pp;
    /* set read plane */
    out_port_b(VGA_GC_INDEX, 4);
    out_port_b(VGA_GC_DATA, pp);
    /* set write plane */
    out_port_b(VGA_SEQ_INDEX, 2);
    out_port_b(VGA_SEQ_DATA, pmask);
}
