const VGA_PALETTE_INDEX: u32 = 0x3C8;

static mut PALETTE_256_BACKUP_DATA: [u8; 256 * 3] = [0; 256 * 3];

fn vga_backup_palette_256() {
    // Set the palette index to 0 to start reading colors
    out_port_b(VGA_PALETTE_INDEX, 0);

    // Read the palette data for all 256 colors
    unsafe {
        for i in 0..256 {
            // Read the red component
            PALETTE_256_BACKUP_DATA[i * 3] = in_port_b(VGA_PALETTE_DATA);
            // Read the green component
            PALETTE_256_BACKUP_DATA[i * 3 + 1] = in_port_b(VGA_PALETTE_DATA);
            // Read the blue component
            PALETTE_256_BACKUP_DATA[i * 3 + 2] = in_port_b(VGA_PALETTE_DATA);
        }
    }
}

fn vga_restore_palette_256() {
    // Set the palette index to 0 to start writing colors
    out_port_b(VGA_PALETTE_INDEX, 0);

    // Write the palette data for all 256 colors
    unsafe {
        for i in 0..256 {
            // Write the red component
            out_port_b(VGA_PALETTE_DATA, PALETTE_256_BACKUP_DATA[i * 3]);
            // Write the green component
            out_port_b(VGA_PALETTE_DATA, PALETTE_256_BACKUP_DATA[i * 3 + 1]);
            // Write the blue component
            out_port_b(VGA_PALETTE_DATA, PALETTE_256_BACKUP_DATA[i * 3 + 2]);
        }
    }
}

const PALETTE_SIZE: usize = 16; // Text mode uses 16 colors
static mut ORIGINAL_TEXT_PALETTE: [u8; PALETTE_SIZE * 3] = [0; PALETTE_SIZE * 3]; // 3 bytes per color

fn vga_backup_text_mode_palette() {
    out_port_b(0x3C7, 0); // Set the palette read address to 0
    kprint!("Backing up text mode palette:\n");
    unsafe {
        for i in 0..PALETTE_SIZE * 3 {
            ORIGINAL_TEXT_PALETTE[i] = in_port_b(0x3C9);
            if i % 3 == 0 {
                // New line for each color entry for readability
                kprint!("\nColor {}: ", i / 3);
            }
            kprint!("{:x} ", ORIGINAL_TEXT_PALETTE[i]);
        }
    }
    kprint!("\n"); // New line after finishing backup
}

fn vga_restore_text_mode_palette() {
    out_port_b(0x3C8, 0); // Set the palette write address to 0
    kprint!("Restoring text mode palette:\n");
    unsafe {
        for i in 0..PALETTE_SIZE * 3 {
            out_port_b(0x3C9, ORIGINAL_TEXT_PALETTE[i]);
            if i % 3 == 0 {
                // New line for each color entry for readability
                kprint!("\nColor {}: ", i / 3);
            }
            kprint!("{:x} ", ORIGINAL_TEXT_PALETTE[i]);
        }
    }
    kprint!("\n"); // New line after finishing restoration
}

const VGA_TEXT_MODE_SIZE: usize = 80 * 25;
static mut VGA_TEXT_MODE_BACKUP: [u16; VGA_TEXT_MODE_SIZE] = [0; VGA_TEXT_MODE_SIZE];

fn vga_backup_vidmem() {
    let text_ptr: *const u16 = (0xffff80003fc00000 + REGION3) as *const u16;
    unsafe {
        for i in 0..VGA_TEXT_MODE_SIZE {
            VGA_TEXT_MODE_BACKUP[i] = *text_ptr.add(i);
        }
    }
}

fn vga_restore_vidmem() {
    let text_ptr: *mut u16 = (0xffff80003fc00000 + REGION3) as *mut u16;
    unsafe {
        for i in 0..VGA_TEXT_MODE_SIZE {
            core::ptr::write_volatile(text_ptr.add(i), VGA_TEXT_MODE_BACKUP[i]);
        }
    }
}

#[repr(C)]
#[repr(packed(2))]
#[derive(Clone, Copy)]
struct Buffer {
    buf: [u8; VGA_SCREEN_SIZE],
}

static mut _SBUFFERS: [Buffer; 2] = [Buffer {
    buf: [0; VGA_SCREEN_SIZE],
}; 2];
static mut _SBACK: usize = 0;

pub fn vga_flip() {
    unsafe {
        core::ptr::write_volatile(
            (0xffff80003fc00000 + VGA_MEM_ADDR) as *mut Buffer,
            _SBUFFERS[_SBACK],
        );

        _SBACK = 1 - _SBACK;
    }
}

pub fn vga_enter() {
    vga_backup_palette_256();
    vga_backup_vidmem();
    vga_backup_text_mode_palette();
    vga_write_regs(true);
}

pub fn vga_exit() {
    out_port_b(VGA_SEQ_INDEX, 0x01);
    let seq1 = in_port_b(VGA_SEQ_DATA);
    out_port_b(VGA_SEQ_DATA, seq1 | 0x20); // Set bit 5 of Sequencer register 1 to 1 (Screen Off)

    vga_write_regs(false);
    vga_restore_palette_256();
    vga_restore_text_mode_palette();
    vga_write_font(16);

    // Re-enable video output after the switch
    out_port_b(VGA_SEQ_INDEX, 0x01);
    out_port_b(VGA_SEQ_DATA, seq1 & !0x20); // Reset bit 5 to 0 (Screen On)

    vga_restore_vidmem();
}

pub fn vga_clear_screen() {
    for i in 0..VGA_SCREEN_WIDTH {
        for j in 0..VGA_SCREEN_HEIGHT {
            vga_plot_pixel(i, j, 0x0f);
        }
    }
    vga_flip();

    for i in 0..VGA_SCREEN_WIDTH {
        for j in 0..VGA_SCREEN_HEIGHT {
            vga_plot_pixel(i, j, 0x0f);
        }
    }
    vga_flip();
}

pub fn vga_plot_pixel(x: u32, y: u32, color: u8) {
    let offset = (x + VGA_SCREEN_WIDTH * y) as usize;

    unsafe {
        _SBUFFERS[_SBACK].buf[offset] = color;
    }
}

